    SELECT 'AREA_MANAGEMENT',     'Quản lý khu vực' FROM DUAL UNION ALL
    SELECT 'COURT_MANAGEMENT',    'Quản lý sân con' FROM DUAL UNION ALL
    SELECT 'CUSTOMER_MANAGEMENT', 'Quản lý khách hàng' FROM DUAL UNION ALL
    SELECT 'EMPLOYEE_MANAGEMENT', 'Quản lý nhân viên' FROM DUAL UNION ALL
    SELECT 'PRICE_MANAGEMENT',    'Quản lý bảng giá' FROM DUAL UNION ALL
    SELECT 'BOOKING_MANAGEMENT',  'Quản lý đặt sân' FROM DUAL UNION ALL
    SELECT 'SERVICE_MANAGEMENT', 'Cung cấp dịch vụ' FROM DUAL UNION ALL
    SELECT 'INVOICE_MANAGEMENT',  'Quản lý hóa đơn' FROM DUAL UNION ALL
    SELECT 'PRODUCT_MANAGEMENT',  'Quản lý sản phẩm' FROM DUAL UNION ALL
