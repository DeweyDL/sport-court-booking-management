-- 4) ACCOUNT_ROLE_GROUP_MAPPING (auto MAPPING_ID: "MAPPING - <number>") - compatible PL/SQL style
DECLARE
    v_next_map_no NUMBER;
    v_exists NUMBER;
    v_role_id ROLE.ROLE_ID%TYPE;
BEGIN
    UPDATE ACCOUNT_ROLE_GROUP_MAPPING
    SET IS_DELETED = 1
    WHERE GROUP_ID IN ('OWNER', 'BRANCH_MANAGER', 'CASHIER', 'CUSTOMER')
      AND IS_DELETED = 0;

    SELECT NVL(MAX(TO_NUMBER(REGEXP_SUBSTR(MAPPING_ID, '[0-9]+$'))), 0)
    INTO v_next_map_no
    FROM ACCOUNT_ROLE_GROUP_MAPPING
    WHERE REGEXP_LIKE(MAPPING_ID, '^MAPPING - [0-9]+$');

    FOR rec IN (
        SELECT 'OWNER' AS GROUP_ID, 'OWNER_ACCOUNT_MANAGEMENT' AS ROLE_NAME FROM DUAL UNION ALL
        SELECT 'OWNER', 'OWNER_REPORT_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'OWNER', 'OWNER_BRANCH_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'OWNER', 'OWNER_AREA_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'OWNER', 'OWNER_COURT_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'OWNER', 'OWNER_PRICE_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'OWNER', 'OWNER_BOOKING_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'OWNER', 'OWNER_SERVICE_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'OWNER', 'OWNER_INVOICE_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'OWNER', 'OWNER_CUSTOMER_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'OWNER', 'OWNER_EMPLOYEE_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'OWNER', 'OWNER_PRODUCT_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'OWNER', 'OWNER_EQUIPMENT_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'OWNER', 'OWNER_IMPORT_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'OWNER', 'OWNER_FINANCE_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'BRANCH_MANAGER', 'BRANCH_MANAGER_BRANCH_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'BRANCH_MANAGER', 'BRANCH_MANAGER_REPORT_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'BRANCH_MANAGER', 'BRANCH_MANAGER_AREA_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'BRANCH_MANAGER', 'BRANCH_MANAGER_COURT_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'BRANCH_MANAGER', 'BRANCH_MANAGER_PRICE_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'BRANCH_MANAGER', 'BRANCH_MANAGER_BOOKING_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'BRANCH_MANAGER', 'BRANCH_MANAGER_SERVICE_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'BRANCH_MANAGER', 'BRANCH_MANAGER_INVOICE_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'BRANCH_MANAGER', 'BRANCH_MANAGER_CUSTOMER_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'BRANCH_MANAGER', 'BRANCH_MANAGER_EMPLOYEE_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'BRANCH_MANAGER', 'BRANCH_MANAGER_PRODUCT_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'BRANCH_MANAGER', 'BRANCH_MANAGER_EQUIPMENT_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'BRANCH_MANAGER', 'BRANCH_MANAGER_IMPORT_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'BRANCH_MANAGER', 'BRANCH_MANAGER_FINANCE_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'CASHIER', 'CASHIER_BOOKING_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'CASHIER', 'CASHIER_SERVICE_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'CASHIER', 'CASHIER_INVOICE_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'CASHIER', 'CASHIER_IMPORT_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'CUSTOMER', 'CUSTOMER_BOOKING_MANAGEMENT' FROM DUAL UNION ALL
        SELECT 'CUSTOMER', 'CUSTOMER_PROFILE_MANAGEMENT' FROM DUAL
    ) LOOP
        BEGIN
            SELECT r.ROLE_ID
            INTO v_role_id
            FROM ROLE r
            WHERE r.ROLE_NAME = rec.ROLE_NAME
              AND r.IS_DELETED = 0;
        EXCEPTION
            WHEN NO_DATA_FOUND THEN
                v_role_id := NULL;
        END;

        IF v_role_id IS NOT NULL THEN
            SELECT COUNT(1)
            INTO v_exists
            FROM ACCOUNT_ROLE_GROUP_MAPPING m
            WHERE m.GROUP_ID = rec.GROUP_ID
              AND m.ROLE_ID = v_role_id
              AND m.IS_DELETED = 0;

            IF v_exists = 0 THEN
                v_next_map_no := v_next_map_no + 1;
                INSERT INTO ACCOUNT_ROLE_GROUP_MAPPING (
                    MAPPING_ID, ROLE_ID, GROUP_ID, CREATED_AT, IS_DELETED
                ) VALUES (
                    'MAPPING - ' || v_next_map_no,
                    v_role_id,
                    rec.GROUP_ID,
                    SYSDATE,
                    0
                );
            END IF;
        END IF;
    END LOOP;
END;
