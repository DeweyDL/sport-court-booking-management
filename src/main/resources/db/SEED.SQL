-- 2) ROLE_GROUP (business codes; required by runtime and register flow)
INSERT INTO ROLE_GROUP (GROUP_ID, GROUP_NAME, DESCRIPTION, CREATED_AT, IS_DELETED)
SELECT s.GROUP_ID, s.GROUP_NAME, s.DESCRIPTION, SYSDATE, 0
FROM (
    SELECT 'OWNER' AS GROUP_ID, 'Chủ sân' AS GROUP_NAME, 'Toàn quyền hệ thống' AS DESCRIPTION FROM DUAL UNION ALL
    SELECT 'BRANCH_MANAGER', 'Quản lý chi nhánh', 'Quản lý dữ liệu trong chi nhánh' FROM DUAL UNION ALL
    SELECT 'CASHIER', 'Thu ngân', 'Thao tác nghiệp vụ tại quầy' FROM DUAL UNION ALL
    SELECT 'CUSTOMER', 'Khách hàng', 'Sử dụng chức năng khách hàng' FROM DUAL
) s
WHERE NOT EXISTS (
    SELECT 1
    FROM ROLE_GROUP rg
    WHERE rg.GROUP_ID = s.GROUP_ID
      AND rg.IS_DELETED = 0
);

-- 3) ROLE (auto ROLE_ID: "ROLE - <number>") - compatible PL/SQL style
DECLARE
    v_next_role_no NUMBER;
    v_exists NUMBER;
BEGIN
    SELECT NVL(MAX(TO_NUMBER(REGEXP_SUBSTR(ROLE_ID, '[0-9]+$'))), 0)
    INTO v_next_role_no
    FROM ROLE
    WHERE REGEXP_LIKE(ROLE_ID, '^ROLE - [0-9]+$');

    FOR rec IN (
        SELECT 'ACCOUNT_MANAGEMENT' AS FUNCTION_ID, 'OWNER_ACCOUNT_MANAGEMENT' AS ROLE_NAME, 1 AS CAN_VIEW, 1 AS CAN_ADD, 1 AS CAN_EDIT, 1 AS CAN_DELETE, 1 AS CAN_DOWNLOAD FROM DUAL UNION ALL
        SELECT 'REPORT_MANAGEMENT', 'OWNER_REPORT_MANAGEMENT', 1, 1, 1, 1, 1 FROM DUAL UNION ALL
        SELECT 'BRANCH_MANAGEMENT', 'OWNER_BRANCH_MANAGEMENT', 1, 1, 1, 1, 1 FROM DUAL UNION ALL
        SELECT 'AREA_MANAGEMENT', 'OWNER_AREA_MANAGEMENT', 1, 1, 1, 1, 1 FROM DUAL UNION ALL
        SELECT 'COURT_MANAGEMENT', 'OWNER_COURT_MANAGEMENT', 1, 1, 1, 1, 1 FROM DUAL UNION ALL
        SELECT 'PRICE_MANAGEMENT', 'OWNER_PRICE_MANAGEMENT', 1, 1, 1, 1, 1 FROM DUAL UNION ALL
        SELECT 'BOOKING_MANAGEMENT', 'OWNER_BOOKING_MANAGEMENT', 1, 1, 1, 1, 1 FROM DUAL UNION ALL
        SELECT 'SERVICE_MANAGEMENT', 'OWNER_SERVICE_MANAGEMENT', 1, 1, 1, 1, 1 FROM DUAL UNION ALL
        SELECT 'INVOICE_MANAGEMENT', 'OWNER_INVOICE_MANAGEMENT', 1, 1, 1, 1, 1 FROM DUAL UNION ALL
        SELECT 'CUSTOMER_MANAGEMENT', 'OWNER_CUSTOMER_MANAGEMENT', 1, 1, 1, 1, 1 FROM DUAL UNION ALL
        SELECT 'EMPLOYEE_MANAGEMENT', 'OWNER_EMPLOYEE_MANAGEMENT', 1, 1, 1, 1, 1 FROM DUAL UNION ALL
        SELECT 'PRODUCT_MANAGEMENT', 'OWNER_PRODUCT_MANAGEMENT', 1, 1, 1, 1, 1 FROM DUAL UNION ALL
        SELECT 'EQUIPMENT_MANAGEMENT', 'OWNER_EQUIPMENT_MANAGEMENT', 1, 1, 1, 1, 1 FROM DUAL UNION ALL
        SELECT 'IMPORT_MANAGEMENT', 'OWNER_IMPORT_MANAGEMENT', 1, 1, 1, 1, 1 FROM DUAL UNION ALL
        SELECT 'FINANCE_MANAGEMENT', 'OWNER_FINANCE_MANAGEMENT', 1, 1, 1, 1, 1 FROM DUAL UNION ALL
        SELECT 'REPORT_MANAGEMENT', 'BRANCH_MANAGER_REPORT_MANAGEMENT', 1, 1, 1, 1, 1 FROM DUAL UNION ALL
        SELECT 'BRANCH_MANAGEMENT', 'BRANCH_MANAGER_BRANCH_MANAGEMENT', 1, 1, 1, 1, 1 FROM DUAL UNION ALL
        SELECT 'AREA_MANAGEMENT', 'BRANCH_MANAGER_AREA_MANAGEMENT', 1, 1, 1, 1, 1 FROM DUAL UNION ALL
        SELECT 'COURT_MANAGEMENT', 'BRANCH_MANAGER_COURT_MANAGEMENT', 1, 1, 1, 1, 1 FROM DUAL UNION ALL
        SELECT 'PRICE_MANAGEMENT', 'BRANCH_MANAGER_PRICE_MANAGEMENT', 1, 1, 1, 1, 1 FROM DUAL UNION ALL
        SELECT 'BOOKING_MANAGEMENT', 'BRANCH_MANAGER_BOOKING_MANAGEMENT', 1, 1, 1, 1, 1 FROM DUAL UNION ALL
        SELECT 'SERVICE_MANAGEMENT', 'BRANCH_MANAGER_SERVICE_MANAGEMENT', 1, 1, 1, 1, 1 FROM DUAL UNION ALL
        SELECT 'INVOICE_MANAGEMENT', 'BRANCH_MANAGER_INVOICE_MANAGEMENT', 1, 1, 1, 1, 1 FROM DUAL UNION ALL
        SELECT 'CUSTOMER_MANAGEMENT', 'BRANCH_MANAGER_CUSTOMER_MANAGEMENT', 1, 1, 1, 1, 1 FROM DUAL UNION ALL
        SELECT 'EMPLOYEE_MANAGEMENT', 'BRANCH_MANAGER_EMPLOYEE_MANAGEMENT', 1, 1, 1, 1, 1 FROM DUAL UNION ALL
        SELECT 'PRODUCT_MANAGEMENT', 'BRANCH_MANAGER_PRODUCT_MANAGEMENT', 1, 1, 1, 1, 1 FROM DUAL UNION ALL
        SELECT 'EQUIPMENT_MANAGEMENT', 'BRANCH_MANAGER_EQUIPMENT_MANAGEMENT', 1, 1, 1, 1, 1 FROM DUAL UNION ALL
        SELECT 'IMPORT_MANAGEMENT', 'BRANCH_MANAGER_IMPORT_MANAGEMENT', 1, 1, 1, 1, 1 FROM DUAL UNION ALL
        SELECT 'FINANCE_MANAGEMENT', 'BRANCH_MANAGER_FINANCE_MANAGEMENT', 1, 1, 1, 1, 1 FROM DUAL UNION ALL
        SELECT 'BOOKING_MANAGEMENT', 'CASHIER_BOOKING_MANAGEMENT', 0, 0, 0, 1, 1 FROM DUAL UNION ALL
        SELECT 'SERVICE_MANAGEMENT', 'CASHIER_SERVICE_MANAGEMENT', 1, 1, 1, 0, 0 FROM DUAL UNION ALL
        SELECT 'INVOICE_MANAGEMENT', 'CASHIER_INVOICE_MANAGEMENT', 1, 1, 0, 1, 1 FROM DUAL UNION ALL
        SELECT 'IMPORT_MANAGEMENT', 'CASHIER_IMPORT_MANAGEMENT', 1, 1, 0, 1, 1 FROM DUAL UNION ALL
        SELECT 'BOOKING_MANAGEMENT', 'CUSTOMER_BOOKING_MANAGEMENT', 1, 1, 1, 0, 0 FROM DUAL UNION ALL
        SELECT 'CUSTOMER_MANAGEMENT', 'CUSTOMER_PROFILE_MANAGEMENT', 0, 0, 1, 0, 0 FROM DUAL
    ) LOOP
        SELECT COUNT(1)
        INTO v_exists
        FROM ROLE r
        WHERE r.FUNCTION_ID = rec.FUNCTION_ID
          AND r.ROLE_NAME = rec.ROLE_NAME
          AND r.IS_DELETED = 0;

        IF v_exists = 0 THEN
            v_next_role_no := v_next_role_no + 1;
            INSERT INTO ROLE (
                ROLE_ID, FUNCTION_ID, ROLE_NAME,
                CAN_ADD, CAN_EDIT, CAN_DELETE, CAN_DOWNLOAD, CAN_VIEW,
                CREATED_AT, IS_DELETED
            ) VALUES (
                'ROLE - ' || v_next_role_no,
                rec.FUNCTION_ID,
                rec.ROLE_NAME,
                rec.CAN_ADD, rec.CAN_EDIT, rec.CAN_DELETE, rec.CAN_DOWNLOAD, rec.CAN_VIEW,
                SYSDATE,
                0
            );
        ELSE
            UPDATE ROLE
            SET FUNCTION_ID = rec.FUNCTION_ID,
                CAN_ADD = rec.CAN_ADD,
                CAN_EDIT = rec.CAN_EDIT,
                CAN_DELETE = rec.CAN_DELETE,
                CAN_DOWNLOAD = rec.CAN_DOWNLOAD,
                CAN_VIEW = rec.CAN_VIEW
            WHERE ROLE_NAME = rec.ROLE_NAME
              AND IS_DELETED = 0;
        END IF;
    END LOOP;
END;
