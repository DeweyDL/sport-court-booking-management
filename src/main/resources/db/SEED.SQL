-- SEED PERMISSION MODEL (Oracle)
-- Idempotent: run many times without duplicate active records.
--
-- IMPORTANT COMPATIBILITY NOTE
-- - FUNCTIONS.FUNCTION_ID and ROLE_GROUP.GROUP_ID are kept as
--   business codes (ex: ACCOUNT_MANAGEMENT, OWNER, CUSTOMER)
--   because application code checks these exact values.
-- - Auto-increment style IDs are applied to:
--   ROLE.ROLE_ID            => "ROLE - <number>"
--   ACCOUNT_ROLE_GROUP_MAPPING.MAPPING_ID => "MAPPING - <number>"
-- ============================================================

-- 1) FUNCTIONS (business codes used directly in Java permission checks)
