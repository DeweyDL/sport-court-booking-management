--    DT_THUE_SAN va DT_DICH_VU duoc phan bo theo TONGTIEN thuc te sau giam gia.
-- ============================================================
DECLARE
    PROCEDURE UPSERT_DOANH_THU(
        P_MADT IN DOANH_THU.MADT%TYPE,
        P_MACN IN DOANH_THU.MACN%TYPE,
        P_LOAI IN DOANH_THU.LOAI%TYPE,
        P_NOIDUNG IN DOANH_THU.NOIDUNG%TYPE,
        P_NGAY IN DOANH_THU.NGAY%TYPE,
        P_NGAY_BAT_DAU IN DOANH_THU.NGAY_BAT_DAU%TYPE,
        P_NGAY_KET_THUC IN DOANH_THU.NGAY_KET_THUC%TYPE
    ) IS
        V_DT_THUE_SAN DOANH_THU.DT_THUE_SAN%TYPE;
        V_DT_DICH_VU DOANH_THU.DT_DICH_VU%TYPE;
        V_TONG DOANH_THU.TONGDOANHTHU%TYPE;
    BEGIN
        SELECT NVL(SUM(DT_THUE_SAN_NET), 0),
               NVL(SUM(DT_DICH_VU_NET), 0),
               NVL(SUM(TONGTIEN), 0)
        INTO V_DT_THUE_SAN, V_DT_DICH_VU, V_TONG
        FROM (
                 SELECT HD.MAHD,
                        NVL(HD.TONGTIEN, 0) AS TONGTIEN,
                        CASE
                            WHEN (NVL(TS.TIEN_THUE_SAN, 0) + NVL(DV.TIEN_DICH_VU, 0)) = 0 THEN 0
                            ELSE ROUND(NVL(HD.TONGTIEN, 0) * NVL(TS.TIEN_THUE_SAN, 0)
                                           / (NVL(TS.TIEN_THUE_SAN, 0) + NVL(DV.TIEN_DICH_VU, 0)), 2)
                            END AS DT_THUE_SAN_NET,
                        CASE
                            WHEN (NVL(TS.TIEN_THUE_SAN, 0) + NVL(DV.TIEN_DICH_VU, 0)) = 0 THEN 0
                            ELSE NVL(HD.TONGTIEN, 0)
                                - ROUND(NVL(HD.TONGTIEN, 0) * NVL(TS.TIEN_THUE_SAN, 0)
                                            / (NVL(TS.TIEN_THUE_SAN, 0) + NVL(DV.TIEN_DICH_VU, 0)), 2)
                            END AS DT_DICH_VU_NET
                 FROM HOA_DON HD
                          JOIN NHAN_VIEN NV ON NV.MANV = HD.MANV
                          LEFT JOIN (
                     SELECT MAHD, SUM(DON_GIA_THUE) AS TIEN_THUE_SAN
                     FROM CHI_TIET_HOA_DON_THUE_SAN
                     WHERE IS_DELETED = 0
                       AND TRANGTHAI <> 'ĐÃ HUỶ'
                     GROUP BY MAHD
                 ) TS ON TS.MAHD = HD.MAHD
                          LEFT JOIN (
                     SELECT MAHD, SUM(SL * DON_GIA) AS TIEN_DICH_VU
                     FROM CHI_TIET_HOA_DON_DICH_VU_DA_DUNG
                     WHERE IS_DELETED = 0
                     GROUP BY MAHD
                 ) DV ON DV.MAHD = HD.MAHD
                 WHERE NV.MACN = P_MACN
                   AND TRUNC(HD.CREATED_AT) BETWEEN TRUNC(P_NGAY_BAT_DAU) AND TRUNC(P_NGAY_KET_THUC)
                   AND HD.TRANGTHAI = 'ĐÃ THANH TOÁN'
                   AND HD.IS_DELETED = 0
             );

        V_DT_THUE_SAN := ROUND(V_DT_THUE_SAN, 2);
        V_TONG := ROUND(V_TONG, 2);
        V_DT_DICH_VU := ROUND(V_TONG - V_DT_THUE_SAN, 2);

        UPDATE DOANH_THU
        SET MACN = P_MACN,
            LOAI = P_LOAI,
            NOIDUNG = P_NOIDUNG,
            NGAY = TRUNC(P_NGAY),
            NGAY_BAT_DAU = TRUNC(P_NGAY_BAT_DAU),
            NGAY_KET_THUC = TRUNC(P_NGAY_KET_THUC),
            DT_THUE_SAN = V_DT_THUE_SAN,
            DT_DICH_VU = V_DT_DICH_VU,
            TONGDOANHTHU = V_TONG,
            IS_DELETED = 0
        WHERE MADT = P_MADT;

        IF SQL%ROWCOUNT = 0 THEN
            INSERT INTO DOANH_THU
                (MADT, MACN, LOAI, NOIDUNG, NGAY, NGAY_BAT_DAU, NGAY_KET_THUC,
                 DT_THUE_SAN, DT_DICH_VU, TONGDOANHTHU, CREATED_AT, IS_DELETED)
            VALUES
                (P_MADT, P_MACN, P_LOAI, P_NOIDUNG, TRUNC(P_NGAY), TRUNC(P_NGAY_BAT_DAU), TRUNC(P_NGAY_KET_THUC),
                 V_DT_THUE_SAN, V_DT_DICH_VU, V_TONG, SYSDATE, 0);
        END IF;
    END;
