    PROCEDURE UPSERT_BANG_GIA(
        P_MABG IN BANG_GIA.MABG%TYPE,
        P_MAKV IN BANG_GIA.MAKV%TYPE,
        P_GIOBATDAU IN BANG_GIA.GIOBATDAU%TYPE,
        P_GIA IN BANG_GIA.GIA%TYPE
    ) IS
        V_MABG BANG_GIA.MABG%TYPE;
    BEGIN
        BEGIN
            SELECT MABG
            INTO V_MABG
            FROM BANG_GIA
            WHERE MAKV = P_MAKV
              AND GIOBATDAU = P_GIOBATDAU
              AND GIOKETTHUC = P_GIOBATDAU + 1
              AND IS_DELETED = 0
                FETCH FIRST 1 ROW ONLY;

            UPDATE BANG_GIA
            SET GIA = P_GIA
            WHERE MABG = V_MABG;
        EXCEPTION
            WHEN NO_DATA_FOUND THEN
                UPDATE BANG_GIA
                SET MAKV = P_MAKV,
                    GIOBATDAU = P_GIOBATDAU,
                    GIOKETTHUC = P_GIOBATDAU + 1,
                    GIA = P_GIA,
                    IS_DELETED = 0
                WHERE MABG = P_MABG;

                IF SQL%ROWCOUNT = 0 THEN
                    INSERT INTO BANG_GIA (MABG, MAKV, GIOBATDAU, GIOKETTHUC, GIA, CREATED_AT, IS_DELETED)
                    VALUES (P_MABG, P_MAKV, P_GIOBATDAU, P_GIOBATDAU + 1, P_GIA, SYSDATE, 0);
                END IF;
        END;
    END;

    PROCEDURE SEED_BANG_GIA_24H(
        P_MAKV IN KHU_VUC.MAKV%TYPE,
        P_BG_START IN NUMBER,
        P_GIA_THAP_DIEM IN BANG_GIA.GIA%TYPE,
        P_GIA_BINH_THUONG IN BANG_GIA.GIA%TYPE,
        P_GIA_CAO_DIEM IN BANG_GIA.GIA%TYPE,
        P_GIA_TOI IN BANG_GIA.GIA%TYPE
    ) IS
        V_GIA BANG_GIA.GIA%TYPE;
        V_MABG BANG_GIA.MABG%TYPE;
    BEGIN
        FOR H IN 0 .. 23 LOOP
            IF H BETWEEN 0 AND 5 THEN
                V_GIA := P_GIA_THAP_DIEM;
            ELSIF H BETWEEN 17 AND 19 THEN
                V_GIA := P_GIA_CAO_DIEM;
            ELSIF H BETWEEN 20 AND 23 THEN
                V_GIA := P_GIA_TOI;
            ELSE
                V_GIA := P_GIA_BINH_THUONG;
            END IF;

            V_MABG := 'BG-' || TO_CHAR(P_BG_START + H);
            UPSERT_BANG_GIA(V_MABG, P_MAKV, H, V_GIA);
        END LOOP;
    END;

