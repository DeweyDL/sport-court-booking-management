CREATE OR REPLACE TRIGGER TRG_BIU_ELEMENTS_HOA_DON
    BEFORE INSERT OR UPDATE OF MAKH, GIAMGIA, TIEN_COC, TONGGIATRI
    ON HOA_DON
    FOR EACH ROW
DECLARE
    V_MA_HANG       KHACH_HANG.MA_HANG%TYPE;
    V_CHIET_KHAU    HANG_KHACH_HANG.CHIET_KHAU%TYPE;
    V_TIEN_THUE_SAN NUMBER(12, 2) := 0;
BEGIN
    SELECT MA_HANG
    INTO V_MA_HANG
    FROM KHACH_HANG
    WHERE MAKH = :NEW.MAKH
      AND IS_DELETED = 0;

    IF V_MA_HANG IS NOT NULL THEN
        SELECT CHIET_KHAU
        INTO V_CHIET_KHAU
        FROM HANG_KHACH_HANG
        WHERE MA_HANG = V_MA_HANG
          AND IS_DELETED = 0;
        :NEW.GIAMGIA := V_CHIET_KHAU;
    end if;

    IF NVL(:NEW.GIAMGIA, 0) < 0 OR NVL(:NEW.GIAMGIA, 0) > 100 THEN
        RAISE_APPLICATION_ERROR(
                -20057,
                'GIAMGIA phải nằm trong khoảng từ 0 đến 100.'
        );
    end IF;

    SELECT NVL(SUM(BG.GIA), 0)
    INTO V_TIEN_THUE_SAN
    FROM CHI_TIET_HOA_DON_THUE_SAN CT
             JOIN SAN_CON SC
                  ON SC.MASAN = CT.MASAN
                      AND SC.IS_DELETED = 0
             JOIN BANG_GIA BG
                  ON BG.MAKV = SC.MAKV
                      AND BG.MAKG = CT.MAKG
                      AND BG.IS_DELETED = 0
    WHERE CT.MAHD = :NEW.MAHD
      AND CT.IS_DELETED = 0
      AND CT.TRANGTHAI <> 'ĐÃ HUỶ';

    IF NVL(:NEW.TIEN_COC, 0) > 0 THEN
        IF ABS(:NEW.TIEN_COC - ROUND(V_TIEN_THUE_SAN * 0.7, 2)) > 0.01 THEN
            RAISE_APPLICATION_ERROR(
                    -20058,
                    'TIEN_COC phải bằng 70% tiền thuê sân.'
            );
        END IF;
    ELSE
        :NEW.TIEN_COC := 0;
    END IF;

    :NEW.TONGTIEN := ROUND(
            :NEW.TONGGIATRI * (1 - NVL(:NEW.GIAMGIA, 0) / 100)
                - NVL(:NEW.TIEN_COC, 0),
            2
                     );

    IF :NEW.TONGTIEN < 0 THEN
        RAISE_APPLICATION_ERROR(
                -20059,
                'TONGTIEN không được âm.'
        );
    END IF;
end;
