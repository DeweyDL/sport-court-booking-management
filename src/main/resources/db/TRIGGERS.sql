CREATE OR REPLACE PROCEDURE PRC_DIEU_CHINH_TON_SAN_PHAM(
    P_MASP IN SAN_PHAM.MASP%TYPE,
    P_DELTA IN NUMBER
)
AS
    V_SL_TON SAN_PHAM.SL_TON%TYPE;
BEGIN
    SELECT SL_TON
    INTO V_SL_TON
    FROM SAN_PHAM
    WHERE MASP = P_MASP
      AND IS_DELETED = 0
        FOR UPDATE;

    IF V_SL_TON + P_DELTA < 0 THEN
        RAISE_APPLICATION_ERROR(
                -20090,
                'So luong ton san pham khong du.'
        );
    END IF;

    UPDATE SAN_PHAM
    SET SL_TON = SL_TON + P_DELTA
    WHERE MASP = P_MASP
      AND IS_DELETED = 0;
END;
