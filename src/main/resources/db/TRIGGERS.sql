CREATE OR REPLACE TRIGGER TRG_CHECK_STATUS_HOA_DON
    BEFORE UPDATE OF TRANGTHAI
    ON HOA_DON
    FOR EACH ROW
DECLARE
    V_COUNT PLS_INTEGER := 0;
BEGIN
    IF :NEW.TRANGTHAI = 'ĐÃ THANH TOÁN' THEN
        IF :OLD.TRANGTHAI <> 'CHƯA THANH TOÁN' THEN
            RAISE_APPLICATION_ERROR(-20061, 'Chỉ được xác nhận thanh toán hoá đơn đang ở trạng thái: CHƯA THANH TOÁN');
        end if;
    end if;

    SELECT COUNT(1)
    INTO V_COUNT
    FROM CHI_TIET_HOA_DON_THUE_SAN
    WHERE MAHD = :OLD.MAHD
      AND IS_DELETED = 0
      AND TRANGTHAI = 'ĐÃ XÁC NHẬN';

    IF V_COUNT = 0 THEN
        RAISE_APPLICATION_ERROR(-20062, 'Hoá đơn không có chi tiết thuê sân hợp lệ để thanh toán');
    end if;
end;

